# ============ 5. 获取 Prompt 的函数 ============

def get_system_prompt(style="default"):
    """获取系统提示词"""
    return SYSTEM_PROMPTS.get(style, SYSTEM_PROMPTS["default"])


def get_role_prompt(role):
    """获取角色扮演提示词"""
    return ROLE_PROMPTS.get(role)


def list_styles():
    """列出所有可用的风格"""
    return list(SYSTEM_PROMPTS.keys())


def list_roles():
    """列出所有可用的角色"""
    return list(ROLE_PROMPTS.keys())